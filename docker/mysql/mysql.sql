-- 制药公司核心业务数据库设计
-- 包含：药品信息、库存管理、销售记录
-- 适用于 DeepAgents 结构化数据检索
--
-- 教学场景：
-- 本脚本服务于「数据库查询子智能体与 MySQL 工具」章节，用来准备一个
-- 可被 list_sql_tables、get_table_data、execute_sql_query 查询的业务数据库。
-- 数据库查询助手会先查看真实表名，再读取表结构和样例数据，最后执行 SQL。
--
-- Docker 初始化说明：
-- 当 docker/docker-compose.yaml 把本文件挂载到 /docker-entrypoint-initdb.d/ 时，
-- MySQL 官方镜像只会在数据目录为空的第一次启动时执行本脚本。
-- 如果已经启动过容器并生成了 volume，修改本文件后需要重建数据卷才会重新导入。
--
-- 数据规模：
-- 1. drugs：50 种药品主数据
-- 2. inventory：每种药品 3 个库存批次，共 150 条
-- 3. sales_records：每种药品 2 条销售记录，共 100 条
--
-- 数据边界：
-- 药品名称、治疗领域和剂型参考常见业务场景；批准文号、库存、销售额、
-- 客户名称和销售区域均为教学模拟数据，不用于真实药品合规或经营判断。
--
-- 字符集说明：
-- 本文件使用 UTF-8 编码保存。导入前显式执行 SET NAMES utf8mb4，
-- 是为了确保 MySQL 初始化脚本读取中文字符串时按 utf8mb4 解释，
-- 避免出现 “å¸ƒ...” 这类 UTF-8 字节被错误字符集读入后的乱码。

SET NAMES utf8mb4 COLLATE utf8mb4_unicode_ci;

CREATE DATABASE IF NOT EXISTS deepsearch_db
    DEFAULT CHARACTER SET utf8mb4
    DEFAULT COLLATE utf8mb4_unicode_ci;
USE deepsearch_db;

-- 表关系总览：
-- drugs 是药品主表，保存药品基础信息。
-- inventory 通过 drug_id 关联 drugs，用于回答库存、批次、仓库、效期问题。
-- sales_records 通过 drug_id 关联 drugs，用于回答销量、销售额、区域表现问题。
--
-- 插入顺序要求：
-- inventory 和 sales_records 都依赖 drugs.drug_id 外键。
-- 因此必须先插入 drugs，再插入库存和销售记录。
-- 下方模拟数据中的 drug_id=1..50 依赖空库首次初始化时的自增顺序。

-- 1. 药品信息表 (Drug Details)
-- 记载每一种药品的详情信息
CREATE TABLE drugs (
    drug_id INT PRIMARY KEY AUTO_INCREMENT,
    generic_name VARCHAR(100) NOT NULL,    -- 通用名 (如 布洛芬缓释胶囊)
    brand_name VARCHAR(100),               -- 商品名 (如 芬必得)
    approval_number VARCHAR(50),           -- 批准文号 (国药准字H...)
    specifications VARCHAR(100),           -- 规格 (如 0.3g*24粒/盒)
    dosage_form VARCHAR(50),               -- 剂型 (胶囊/片剂/注射液)
    manufacturer VARCHAR(100),             -- 生产厂家
    therapeutic_area VARCHAR(50),          -- 治疗领域 (如 解热镇痛, 心血管)
    description TEXT,                      -- 药品详情/适应症描述
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- 2. 库存表 (Inventory)
-- 记录药品的库存量，通过 drug_id 关联
CREATE TABLE inventory (
    inventory_id INT PRIMARY KEY AUTO_INCREMENT,
    drug_id INT NOT NULL,

    batch_number VARCHAR(50) NOT NULL,     -- 生产批号 (医药库存核心字段)
    quantity_on_hand INT DEFAULT 0,        -- 当前库存量 (盒/瓶)
    warehouse_location VARCHAR(50),        -- 仓库位置 (如 A区-01架)

    production_date DATE,                  -- 生产日期
    expiry_date DATE,                      -- 有效期至 (用于预警)
    last_updated TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,

    FOREIGN KEY (drug_id) REFERENCES drugs(drug_id) ON DELETE CASCADE
);

-- 3. 药品销售情况表 (Sales Records)
-- 记录每个药品的销售情况，通过 drug_id 关联
CREATE TABLE sales_records (
    sale_id INT PRIMARY KEY AUTO_INCREMENT,
    drug_id INT NOT NULL,

    sale_date DATE NOT NULL,               -- 销售日期
    quantity_sold INT NOT NULL,            -- 销售数量
    unit_price DECIMAL(10, 2),             -- 销售单价
    total_amount DECIMAL(15, 2),           -- 销售总额

    customer_name VARCHAR(100),            -- 客户名称 (如 xx第一人民医院, xx大药房)
    region VARCHAR(50),                    -- 销售区域 (用于区域分析)
    sales_rep VARCHAR(50),                 -- 销售代表

    FOREIGN KEY (drug_id) REFERENCES drugs(drug_id) ON DELETE CASCADE
);

